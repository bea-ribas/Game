package theGameModel;

public class Human {
	
	private String name;
	private String bornDate;
	
	
	public String getBornDate() {
		
		return bornDate;
	}
	
	public void setBornDate(String bornDate) {
		
		this.bornDate = bornDate;
	}
	
	public String getName() {
		
		return name;
	}
	
	public void setName(String name) {
		
		this.name = name;
	}
}